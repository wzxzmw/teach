package com.seentao.stpedu.common.entity;


public class CenterSerialMax {

	private Integer serialMaxId;
	
	private Integer serialMax;
	
	private Integer serialTime;
	

	public Integer getSerialMaxId() {
		return serialMaxId;
	}

	public void setSerialMaxId(Integer serialMaxId) {
		this.serialMaxId = serialMaxId;
	}
	public Integer getSerialMax() {
		return serialMax;
	}

	public void setSerialMax(Integer serialMax) {
		this.serialMax = serialMax;
	}
	public Integer getSerialTime() {
		return serialTime;
	}

	public void setSerialTime(Integer serialTime) {
		this.serialTime = serialTime;
	}

}
