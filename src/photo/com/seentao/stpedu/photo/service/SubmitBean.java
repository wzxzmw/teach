package com.seentao.stpedu.photo.service;

public class SubmitBean {

	
	private String attachmentMajorType;
	private String attachmentSeconds;
	private String attachmentLink;
	private String attachmentName;
	private String attachmentType;
	public String getAttachmentMajorType() {
		return attachmentMajorType;
	}
	public void setAttachmentMajorType(String attachmentMajorType) {
		this.attachmentMajorType = attachmentMajorType;
	}
	public String getAttachmentSeconds() {
		return attachmentSeconds;
	}
	public void setAttachmentSeconds(String attachmentSeconds) {
		this.attachmentSeconds = attachmentSeconds;
	}
	public String getAttachmentLink() {
		return attachmentLink;
	}
	public void setAttachmentLink(String attachmentLink) {
		this.attachmentLink = attachmentLink;
	}
	public String getAttachmentName() {
		return attachmentName;
	}
	public void setAttachmentName(String attachmentName) {
		this.attachmentName = attachmentName;
	}
	public String getAttachmentType() {
		return attachmentType;
	}
	public void setAttachmentType(String attachmentType) {
		this.attachmentType = attachmentType;
	}
	
	
}
